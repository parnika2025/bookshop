const uploadExcel = asyncHandler(async (req, res) => {
  try {
    if (!req.file) {
      return res.status(400).json({ message: "No file uploaded" });
    }

    const workbook = xlsx.read(req.file.buffer, { type: "buffer" });
    const sheetName = workbook.SheetNames[0];
    const sheet = workbook.Sheets[sheetName];
    const data = xlsx.utils.sheet_to_json(sheet);

    const products = [];

    for (let item of data) {
      const title = item["Title"]?.trim();
      const isbn = item["ISBN-13"];
      const author = item["Author"];
      const publisher = item["Publisher"];
      const price = parseFloat(item["price"]);
      const imageUrl = item["Image_URL"];

      if (!title || !isbn) continue;

      const product = {
        title: title,
        slug: slugify(title),
        description: "Imported from Excel",
        author_name: author || "Unknown",
        price: price || 0,
        current_price: price || 0,
        isbn: isbn.replace(/-/g, "") || isbn,
        category: "General",
        brand: publisher || "Unknown",
        quantity: 100,
        images: [
          {
            url: imageUrl || "",
            public_id:uuidv4() ||  "", // optional
          },
        ],
        tags: "new_arrival",
      };

      products.push(product);
    }

    const savedProducts = await Product.insertMany(products);
    res.status(200).json({
      message: "Products uploaded successfully",
      count: savedProducts.length,
      products: savedProducts,
    });
  } catch (error) {
    console.error("Excel Upload Error:", error);
    res.status(500).json({ message: "Failed to upload Excel", error: error.message });
  }
});

